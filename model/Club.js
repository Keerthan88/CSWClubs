class Club {
    constructor(id, title, owner, description, moderator, firstname, lastname, phonenumber){
        this.id = id;
        this.title = title;
        this.owner = owner;
        this.description = description;
        this.moderator = moderator;
        this.firstname = firstname;
        this.lastname = lastname;
        this.phonenumber = phonenumber;
    }
}

export default Club;